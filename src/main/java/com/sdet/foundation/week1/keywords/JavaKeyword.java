package com.sdet.foundation.week1.keywords;

public class JavaKeyword {
    // Java keywords are reserved words that have a predefined meaning in the Java programming language. They cannot be used as identifiers (such as variable names, class names, or method names) because they are reserved for specific purposes in the language syntax.

    // Here are some examples of Java keywords:

    // 1. Data Types: int, double, boolean, char, byte, short, long, float
    // 2. Control Flow: if, else, switch, case, default
    // 3. Looping: for, while, do
    // 4. Access Modifiers: public, private, protected
    // 5. Class and Object Related: class, interface, extends, implements
    // 6. Exception Handling: try, catch, finally, throw, throws
    // 7. Other Keywords: static, final, abstract, synchronized, volatile, transient, native, strictfp, assert, enum, instanceof, this, super, return, break, continue, new

    public static void main(String[] args) {
        int number = 10; // 'int' is a keyword for declaring an integer variable
        if (number > 5) { // 'if' is a keyword for conditional statements
            System.out.println("Number is greater than 5");
        }
    }
}
